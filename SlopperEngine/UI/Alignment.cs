namespace SlopperEngine.UI;

/// <summary>
/// Alignment options for UI.
/// </summary>
public enum Alignment
{
    /// <summary>
    /// The element should align to the minimum side. (Left or downwards)
    /// </summary>
    Min = 1, 
    /// <summary>
    /// The element should align with the center.
    /// </summary>
    Middle = 0, 
    /// <summary>
    /// The element should align to the maximum side. (Right or upwards)
    /// </summary>
    Max = 2
}